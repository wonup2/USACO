
public class PrintPractice {
	
	public static void main(String[] args) {
		
		System.out.println("Hello");
		System.out.println(3+3);
		System.out.println("3" + "3");
		System.out.println("Hello " + (3 + 5) + " World");
		
		//I am a "cool" guy!
		System.out.println("I am a \"cool\" guy!");
		System.out.println("This is \\\\\\");
		System.out.println("This is a\tstudent");
		System.out.println("This is a\nstudent");
	}
	
	
}
